public class ArithmeticOperations
    {
        public decimal Add(decimal n1, decimal n2)
        {
            return n1 + n2;
        }

        public decimal Subract(decimal n1, decimal n2)
        {
            return n1 - n2;
        }

        public decimal Multiply(decimal n1, decimal n2)
        {
            return n1 * n2;
        }

    }
